local reader = require("resty.ipdb.reader")

function _M.new(self, name)
    return setmetatable({
        db = reader:new(name),
    }, mt)
end

function _M.find(self, ips, language)
    local loc = self.db:find(ips, language)
    local country_name = loc.country_name
    local province_name = loc.region_name
    local city_name = loc.city_name
    local point = ""
    if country_name == "中国" then
        point = loc_coor_map[province_name.."-"..city_name]
    else
        point = loc_coor_map[country_name]
    end
    return {country_name, province_name, city_name, point}
end

function _M.build(self)
    return self.db.meta["build"]
end

function _M.fields(self)
    return table.concat(self.db.meta.fields, ",")
end

function _M.languages(self)
    local lang = {}
    for k, v in pairs(self.db.meta.languages) do
        table.insert(lang, k)
    end
    return table.concat(lang, ",")
end

return _M
